let num1 = 20;
let num2 = 10;

// Adição +

let totalAdicao = num1 + num2;
console.log(totalAdicao);

// Subtração -
let totalSubtracao = num1 - num2;
console.log(totalSubtracao);

// Multiplicação * 
let totalMultiplicao = num1 * num2;
console.log(totalMultiplicao);

// Divisão / 
let totalDivisão = num1 / num2;
console.log(totalDivisão);

// Resto % (mod)
let restoDivisao = num1 % num2; // camel Case 
console.log(restoDivisao);

// Incremento ++ Loops for, do-do while
console.log(num1++)
console.log(++num1);

// Decremento -- (Loops for, do-do while)
console.log(num2--);
console.log(--num2);